package rpl.labman.labman;

/**
 *
 * @author Iyes
 */
public class Lab {
    public int labId;
    private String labName;
    private String labLoc;
    private String labStats;

    public int getLabId() {
        return labId;
    }

    public void setLabId(int labId) {
        this.labId = labId;
    }

    public String getLabName() {
        return labName;
    }

    public void setLabName(String labName) {
        this.labName = labName;
    }

    public String getLabLoc() {
        return labLoc;
    }

    public void setLabLoc(String labLoc) {
        this.labLoc = labLoc;
    }

    public String getLabStats() {
        return labStats;
    }

    public void setLabStats(String labStats) {
        this.labStats = labStats;
    }
    
    
}
